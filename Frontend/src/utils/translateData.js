/* =========================================================
   Translate helpers
========================================================= */

export function translateStemBranch(text = "") {
  const stem = text.charAt(0);
  const branch = text.charAt(1);
  return `${stemMap[stem] || stem} ${branchMap[branch] || branch}`;
}

export function translatePalace(name = "") {
  return palaceMap[name] || name;
}

export function translateStar(name = "") {
  return starMap[name] || name;
}

export function translateBrightness(bright = "") {
  return brightnessMap[bright] || bright;
}

export function translateMutagen(mut = "") {
  return mutagenMap[mut] || mut;
}

export function translateGender(gender = "") {
  if (gender === "男") return "Nam";
  if (gender === "女") return "Nữ";
  return gender;
}

export function translateYinYang(yy = "") {
  if (yy === "阳") return "Dương";
  if (yy === "阴") return "Âm";
  return yy;
}

export function translateMisc(term = "") {
  return miscMap[term] || term;
}

export function translateElementClass(value) {
  if (!value) return "";
  return elementClassMap[value] || value;
}
