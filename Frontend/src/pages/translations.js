export const translateChineseDate = (str) => {
  if (!str) return "";
  const NUMS = {
    "三十": "30", "二十九": "29", "二十八": "28", "二十七": "27", "二十六": "26", "二十五": "25",
    "二十四": "24", "二十三": "23", "二十二": "22", "二十一": "21", "二十": "20",
    "十九": "19", "十八": "18", "十七": "17", "十六": "16", "十五": "15", "十四": "14",
    "十三": "13", "十二": "12", "十一": "11", "十": "10",
    "九": "9", "八": "8", "七": "7", "六": "6", "五": "5", "四": "4", "三": "3", "二": "2", "一": "1", "〇": "0"
  };

  let result = str
    .replace(/年/g, " Năm ")
    .replace(/月/g, " Tháng ")
    .replace(/日/g, " Ngày ");

  Object.keys(NUMS)
    .sort((a, b) => b.length - a.length)
    .forEach((k) => {
      result = result.replace(new RegExp(k, "g"), NUMS[k]);
    });

  Object.entries(HEAVENLY_STEMS).forEach(([k, v]) => {
    result = result.replace(new RegExp(k, "g"), v);
  });
  Object.entries(EARTHLY_BRANCHES).forEach(([k, v]) => {
    result = result.replace(new RegExp(k, "g"), v);
  });

  return result.trim();
};

