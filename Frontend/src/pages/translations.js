export const translateAstrolabe = (raw) => {
  if (!raw) return raw;

  const translateStarsWithMutagen = (stars) => {
    return (stars || []).map((s) => ({
      ...s,
      name: translateStarName(s.name),
      brightness: s.brightness ? getBrightnessAbbr(s.brightness) : undefined,
      mutagen: s.mutagen ? MUTAGEN_MAP[s.mutagen] || s.mutagen : undefined,
    }));
  };

  return {
    ...raw,
    palaces: (raw.palaces || []).map((p) => ({
      ...p,
      name: translatePalace(p.name),
      heavenlyStem: translateStem(p.heavenlyStem),
      earthlyBranch: translateBranch(p.earthlyBranch),
      majorStars: translateStarsWithMutagen(p.majorStars),
      minorStars: translateStarsWithMutagen(p.minorStars),
      adjectiveStars: translateStarsWithMutagen(p.adjectiveStars),
      changsheng12: translateTruongSinh(p.changsheng12),
      boshi12: p.boshi12 ? translateStarName(p.boshi12) : undefined,
      jiangqian12: p.jiangqian12 ? translateThanSat(p.jiangqian12) : undefined,
    })),
    soul: translateSoulStar(raw.soul),
    body: translateSoulStar(raw.body),
    fiveElementsClass: translateFiveElements(raw.fiveElementsClass),
  };
};

