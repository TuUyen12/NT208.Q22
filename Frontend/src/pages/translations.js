// Hàm dịch tên sao (hỗ trợ cả sao có kèm tứ hóa)
export const translateStarName = (name) => {
  if (!name) return name;
  
  // Xử lý các trường hợp đặc biệt: tên sao + ký hiệu tứ hóa (禄, 权, 科, 忌)
  const hoaSymbols = ["禄", "权", "科", "忌"];
  
  for (const hoa of hoaSymbols) {
    if (name.endsWith(hoa)) {
      const starName = name.slice(0, -1); // Bỏ ký tự cuối (ký hiệu tứ hóa)
      const starVi = ALL_STARS[starName] || starName;
      const hoaVi = MUTAGEN_MAP[hoa] || hoa;
      return `${starVi} ${hoaVi}`;
    }
  }

  // Dịch tên sao thông thường
  return ALL_DICT[name] || name;
};

